#!/usr/bin/env python3
"""Test native streaming using EXACT SDK legacy byte sequence.

SDK legacy (run_streaming with 10ms interval) in trace:
    cmd1 sample count [9:10] = 00 01 (not a sample count - seems to be flag)
    cmd1 buf bytes [28:30] = 0f 42 3f (0x0f423f = 999,999)
    cmd2[7:10] = 18 00 00 00 (= 24 LE)
    cmd2[47] = 14 (= 20)
"""
import sys
import os
import time
sys.path.insert(0, os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', '..'))
import picoscope_libusb_full as psf
from picoscope_libusb_full import Range, Channel, Coupling
import usb.core


def run_variant(scope, label, cmd1_bytes, cmd2_bytes, duration_s=3.0):
    print(f"\n=== {label} ===")
    scope._flush_all_buffers()

    scope.device.write(0x01, cmd1_bytes.ljust(64, b'\x00'), timeout=1000)
    scope.device.write(0x01, cmd2_bytes.ljust(64, b'\x00'), timeout=1000)

    for _ in range(3):
        try:
            scope.device.read(0x81, 64, timeout=30)
        except (usb.core.USBTimeoutError, usb.core.USBError):
            break

    trigger = bytes([0x02, 0x07, 0x06, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00])
    scope.device.write(0x01, trigger.ljust(64, b'\x00'), timeout=1000)

    total_bytes = 0
    packet_sizes = []
    timestamps = []
    t0 = time.time()
    while time.time() - t0 < duration_s:
        try:
            raw = bytes(scope.device.read(0x82, 4096, timeout=50))
            if raw:
                total_bytes += len(raw)
                packet_sizes.append(len(raw))
                timestamps.append(time.time() - t0)
        except (usb.core.USBTimeoutError, usb.core.USBError):
            continue
    dt = time.time() - t0

    # Stop (SDK sends 0x0a then 0x99 separately)
    try:
        stop = bytes([0x02, 0x0a, 0x00, 0x85, 0x04, 0x99, 0x00, 0x00, 0x00, 0x0a])
        scope.device.write(0x01, stop.ljust(64, b'\x00'), timeout=1000)
        time.sleep(0.02)
        stop2 = bytes([0x02, 0x85, 0x04, 0x99, 0x00, 0x00, 0x00, 0x0a])
        scope.device.write(0x01, stop2.ljust(64, b'\x00'), timeout=1000)
    except usb.core.USBError:
        pass
    time.sleep(0.3)
    scope._flush_all_buffers()
    time.sleep(0.3)

    print(f"  Duration: {dt:.2f}s  Total: {total_bytes} B  Packets: {len(packet_sizes)}")
    if packet_sizes:
        print(f"  Rate: {total_bytes/dt:.1f} B/s")
        print(f"  Sizes: min={min(packet_sizes)} max={max(packet_sizes)} avg={total_bytes/len(packet_sizes):.1f}")
        if len(timestamps) > 1:
            ivs = [timestamps[i+1] - timestamps[i] for i in range(len(timestamps)-1)]
            print(f"  Inter-pkt ms: min={min(ivs)*1000:.1f} max={max(ivs)*1000:.1f} avg={sum(ivs)/len(ivs)*1000:.1f}")
        # Show first packet
        print(f"  First pkt sizes: {packet_sizes[:10]}")
    return total_bytes, dt


